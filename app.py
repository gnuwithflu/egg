def build_stone(s):
    if not s or s.get("type","") == "":
        return Stone()
    return Stone(type=s["type"], level=int(s["level"]))

def build_artifact(a):
    if not a or a.get("type","") == "":
        return Artifact()
    stones = a.get("stones", [{},{},{}])
    while len(stones) < 3:
        stones.append({"type":"","level":0})
    return Artifact(
        type=a["type"], level=int(a["level"]), rarity=int(a["rarity"]),
        stone1=build_stone(stones[0]),
        stone2=build_stone(stones[1]),
        stone3=build_stone(stones[2])
    )

