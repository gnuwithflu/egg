def build_farm(d):
    """Build a Farm object from a state dict."""
    aset = build_artifact_set(d["artifact_set"])
    f = Farm(
        egg=d["egg"],
        cr=np.array(d["cr"], dtype=float),
        er=list(d["er"]),
        silos=int(d["silos"]),
        habs=list(d["habs"]),
        vehicles=list(d["vehicles"]),
        se=float(d["se"]),
        pe=int(d["pe"]),
        te=int(d["te"]),
        colleggtibles=np.array(d["colleggtibles"], dtype=float),
        pop=float(d["pop"]),
        artifact_set=aset,
        video_doubler=bool(d["video_doubler"]),
        cash=float(d["cash"])
    )
    # Apply events
    for k, v in d.get("events", {}).items():
        if k in f.event_effects:
            f.event_effects[k] = float(v)
    return f

def dict_to_state(d):
    """Convert a state dict to a CLI State object, preserving all fields."""
    f = build_farm(d)
    s = State(farm=f, time_elapsed=float(d["time_elapsed"]),
              eggs_layed=list(d["eggs_layed"]), shifts=int(d["shifts"]))
    s.te_claimed = list(d.get("te_claimed", [te_numbers(e) for e in d["eggs_layed"]]))
    return s

