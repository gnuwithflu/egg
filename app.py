@app.route('/api/upgrade_costs', methods=['POST'])
def get_upgrade_costs():
    d = request.json.get("state", default_state())
    try:
        f = build_farm(d)
        # We need a CLI state for research_available
        cli = dict_to_state(d)
        research_count = sum(d["cr"])

        cr_list = []
        for i in range(56):
            if d["cr"][i] >= max_cr[i]: continue
            if not research_available(cli, i): continue
            cost = cr_prices[i][int(d["cr"][i])] * f.artf.cr_cost * (1-0.05*d["er"][7]) * f.coll_effect("waterballoon") * f.event("cr")
            cr_list.append({
                "index": i, "name": cr_names[i][1], "effect": cr_names[i][2],
                "level": int(d["cr"][i]), "max": int(max_cr[i]),
                "cost": fmt(cost), "cost_raw": float(cost), "tier": int(cr_names[i][0])
            })

        hab_list = []
        for i in range(4):
            lv = int(d["habs"][i])
            if lv >= 19:
                hab_list.append({"index": i, "level": lv, "maxed": True,
                                 "capacity": fmt(hab_capacities[lv])})
                continue
            same = sum(1 for j in range(4) if d["habs"][j] == lv+1)
            cost = hab_prices[lv][same] * (1-0.05*d["er"][5]) * f.coll_effect("flame") * f.event("hab")
            hab_list.append({
                "index": i, "level": lv, "maxed": False,
                "capacity": fmt(hab_capacities[lv]),
                "next_capacity": fmt(hab_capacities[lv+1]),
                "cost": fmt(cost), "cost_raw": float(cost)
            })

        veh_list = []
        max_veh_lv = 16 + int(d["cr"][50])  # mirrors farm.py: 16 + cr[50]
        for i in range(f.max_vehicle_number):
            vl = int(d["vehicles"][i])
            maxed = vl >= max_veh_lv
            if vl > 12:
                cost = vehicle_prices[12][min(vl-12,16)] * (1-0.05*d["er"][6]) * f.coll_effect("lithium") * f.event("veh")
            else:
                same = sum(1 for j in range(f.max_vehicle_number) if d["vehicles"][j] == vl+1)
                cost = vehicle_prices[vl][same] * (1-0.05*d["er"][6]) * f.coll_effect("lithium") * f.event("veh")
            veh_list.append({
                "index": i, "level": vl, "maxed": maxed,
                "capacity": fmt(vehicle_capacities[min(vl,21)]),
                "cost": fmt(cost) if not maxed else "maxed", "cost_raw": float(cost) if not maxed else 0
            })

        silo_info = {
            "count": int(d["silos"]), "maxed": d["silos"] >= 10,
            "cost": fmt(silo_price[d["silos"]]) if d["silos"] < 10 else "maxed"
        }

        # Next tier unlock info
        next_tier_in = None
        for bound in cr_bounds:
            if bound > research_count:
                next_tier_in = int(bound - research_count)
                break

        return jsonify({"cr": cr_list, "habs": hab_list, "vehicles": veh_list, "silo": silo_info,
                        "next_tier_in": next_tier_in})
    except Exception as e:
        import traceback; traceback.print_exc()
        return jsonify({"error": str(e)}), 400

@app.route('/api/metadata', methods=['GET'])
def get_metadata():
    return jsonify({
        "artifact_specs": artifact_specs,
        "stone_specs":    stone_specs,
        "virtue_eggs":    list(virtue_egg_index.keys()),
        "cr_names":       [[int(r[0]), r[1], r[2]] for r in cr_names],
        "max_cr":         [int(x) for x in max_cr],
        "cr_bounds":      [int(x) for x in cr_bounds],
    })

