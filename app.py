@app.route('/api/action/set_artifact', methods=['POST'])
def action_set_artifact():
    """Set one artifact slot. Expects slot (a1-a4) and artifact dict."""
    data  = request.json
    d     = data["state"]
    slot  = data.get("slot")        # "a1" .. "a4"
    art_d = data.get("artifact", {})
    if slot not in ("a1","a2","a3","a4"):
        return jsonify({"error": "Invalid slot"}), 400
    try:
        new_art = build_artifact(art_d)
        # Replace just this slot
        aset_d  = copy.deepcopy(d["artifact_set"])
        def art_to_dict(a):
            return {"type": a.type, "level": int(a.level), "rarity": int(a.rarity),
                    "stones": [{"type": a.stone1.type, "level": int(a.stone1.level)},
                               {"type": a.stone2.type, "level": int(a.stone2.level)},
                               {"type": a.stone3.type, "level": int(a.stone3.level)}]}
        aset_d[slot] = art_to_dict(new_art)
        # Validate the whole set builds ok
        build_artifact_set(aset_d)
        d["artifact_set"] = aset_d
        _rl = {0:'C',1:'R',2:'E',3:'L'}.get(new_art.rarity,'?')
        # Build replayable command: "artifact a1 totem 4 L lunar 3 lunar 3"
        stone_parts = []
        for st in [new_art.stone1, new_art.stone2, new_art.stone3]:
            if st.type:
                stone_parts.append(f"{st.type} {st.level}")
        if new_art.type:
            cmd = f"artifact {slot} {new_art.type} {new_art.level} {_rl}"
            if stone_parts:
                cmd += " " + " ".join(stone_parts)
        else:
            cmd = f"artifact {slot} clear"
        add_log(d, cmd, "")
        return jsonify({"state": serialize(d), "stats": get_farm_stats(d)})
    except Exception as e:
        return jsonify({"error": str(e)}), 400

