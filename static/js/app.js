function calcAwayTime(s) {
  const mins = (60 + 6*(s?.er?.[2]||0)) * (s?.silos||1);
  if (mins < 60)   return `${mins.toFixed(0)}min`;
  if (mins < 1440) return `${(mins/60).toFixed(1)}h`;
  return `${(mins/1440).toFixed(1)}d`;
}

