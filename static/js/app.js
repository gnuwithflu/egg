function refreshEditArtifactEditor(slot, existingStones) {
  const type   = document.getElementById(`eae-${slot}-type`)?.value;
  const level  = document.getElementById(`eae-${slot}-level`)?.value;
  const rarity = document.getElementById(`eae-${slot}-rarity`)?.value;
  const infoEl = document.getElementById(`eae-${slot}-info`);
  const stonesEl = document.getElementById(`eae-${slot}-stones`);
  if (!type) { if(infoEl) infoEl.textContent=''; if(stonesEl) stonesEl.innerHTML=''; return; }
  const key  = `${type}${level}${rarity}`;
  const spec = ARTIFACT_SPECS[key];
  const validRarities = [0,1,2,3].filter(r => ARTIFACT_SPECS[`${type}${level}${r}`]);
  if (!spec) {
    if (infoEl) infoEl.innerHTML = `<span style="color:var(--red)">⚠ T${level}${RARITY_LETTER[rarity]} invalid. Valid: ${validRarities.map(r=>RARITY_NAME[r]).join(', ')}</span>`;
    if (stonesEl) stonesEl.innerHTML = ''; return;
  }
  const numSlots = spec[1];
  if (infoEl) infoEl.innerHTML = `<div class="arti-editor-preview">${artiImg(type,parseInt(level),60)}<span style="color:var(--green)">✓ Effect: ${spec[0]} | ${numSlots} stone slot${numSlots!==1?'s':''}</span></div>`;
  const prev = existingStones || state?.artifact_set?.[slot]?.stones || [];
  if (stonesEl) stonesEl.innerHTML = Array.from({length: numSlots}, (_,i) => {
    const ps = prev[i] || {};
    return `<div class="stone-pair">
      <select id="eae-${slot}-stone-${i}-type">
        <option value="">— none —</option>
        ${STONE_TYPES.map(t=>`<option value="${t}" ${t===ps.type?'selected':''}>${t}</option>`).join('')}
      </select>
      <select id="eae-${slot}-stone-${i}-lv">
        ${[2,3,4].map(n=>`<option value="${n}" ${n===(ps.level||2)?'selected':''}>${'T'+n}</option>`).join('')}
      </select>
    </div>`;
  }).join('');
}

async function applyEditArtifactEditor(slot) {
  const type   = document.getElementById(`eae-${slot}-type`)?.value || '';
  const level  = parseInt(document.getElementById(`eae-${slot}-level`)?.value) || 1;
  const rarity = parseInt(document.getElementById(`eae-${slot}-rarity`)?.value) || 0;
  if (!type) { await clearEditArtifactSlot(slot); return; }
  const key = `${type}${level}${rarity}`;
  const spec = ARTIFACT_SPECS[key];
  if (!spec) { showToast(`Invalid artifact`, true); return; }
  const numSlots = spec[1];
  const stones = Array.from({length:3}, (_,i) => {
    if (i >= numSlots) return {type:'',level:0};
    const t = document.getElementById(`eae-${slot}-stone-${i}-type`)?.value||'';
    const l = parseInt(document.getElementById(`eae-${slot}-stone-${i}-lv`)?.value)||2;
    return {type:t, level:t?l:0};
  });
  await apiAction('/api/action/set_artifact', { slot, artifact:{type,level,rarity,stones} });
  refreshEditArtifactGrid();
}

