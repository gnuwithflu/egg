function saveSnapshot() {
  if (!state) return;
  const n       = snapshots.length + 1;
  const egg     = state.egg || 'curiosity';
  const time    = lastStats?.time || (state.time_elapsed != null ? _fmtTime(state.time_elapsed) : '0s');
  const claimed = state.te || 0;
  const pending = (state.eggs_layed||[]).reduce((sum,e,i) =>
    sum + Math.max(0, teNumbers(e) - (state.te_claimed?.[i]||0)), 0);
  const totalTE = claimed + pending;
  snapshots.push({
    n, egg, time, totalTE, pending,
    state: JSON.parse(JSON.stringify(state))
  });
  renderSnapshots();
  showToast(`Snapshot ${n} saved (${totalTE} TE)`);
}

function renderSnapshots() {
  const el = document.getElementById('snapshots-list');
  if (!snapshots.length) { el.innerHTML = '<div style="font-size:11px;color:var(--text-subtle);padding:4px 0">No snapshots yet</div>'; return; }
  el.innerHTML = snapshots.map((snap, i) => `
    <div class="snapshot-item">
      <span class="snapshot-egg">${eggImg(snap.egg, 18)}</span>
      <span class="snapshot-label" style="color:${EGG_COLOR[snap.egg]||''}" onclick="renameSnapshot(${i})" title="Click to rename">${snap.label || ('#' + snap.n + ' ' + snap.egg)}</span>
      <span class="snapshot-meta">${snap.time} · ${snap.totalTE}${teImg(13)}</span>
      <button class="snapshot-btn-load" onclick="loadSnapshot(${i})" title="Restore this snapshot">↩</button>
      <button class="snapshot-btn-load" onclick="deleteSnapshot(${i})" style="color:var(--red)" title="Delete">×</button>
    </div>`).join('');
}

function renameSnapshot(i) {
  const snap = snapshots[i];
  if (!snap) return;
  const current = snap.label || ('#' + snap.n + ' ' + snap.egg);
  // Find the label element and replace with input
  const items = document.querySelectorAll('.snapshot-item');
  const labelEl = items[i]?.querySelector('.snapshot-label');
  if (!labelEl) return;
  const input = document.createElement('input');
  input.type = 'text';
  input.value = current;
  input.className = 'snapshot-rename-input';
  input.onblur = () => { snap.label = input.value.trim() || current; renderSnapshots(); };
  input.onkeydown = (e) => { if (e.key === 'Enter') input.blur(); if (e.key === 'Escape') { input.value = current; input.blur(); } };
  labelEl.replaceWith(input);
  input.select();
}

async function loadSnapshot(i) {
  const snap = snapshots[i];
  if (!snap) return;
  state = JSON.parse(JSON.stringify(snap.state));
  setLoading(true);
  try {
    const stats = await apiFetch('/api/stats', 'POST', { state });
    renderAll(stats);
    await refreshUpgrades();
    showToast(`Loaded snapshot #${snap.n}`);
  } catch(e) {
    showToast('Failed to load snapshot: ' + e.message, true);
  }
  setLoading(false);
}

function deleteSnapshot(i) { snapshots.splice(i, 1); renderSnapshots(); }

