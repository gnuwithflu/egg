function refreshArtifactEditor(slot, existingStones) {
  const type   = document.getElementById(`ae-${slot}-type`)?.value;
  const level  = document.getElementById(`ae-${slot}-level`)?.value;
  const rarity = document.getElementById(`ae-${slot}-rarity`)?.value;
  const infoEl = document.getElementById(`ae-${slot}-info`);
  const stonesEl = document.getElementById(`ae-${slot}-stones`);

  if (!type) {
    if (infoEl)   infoEl.textContent = '';
    if (stonesEl) stonesEl.innerHTML = '';
    return;
  }

  const key  = `${type}${level}${rarity}`;
  const spec = ARTIFACT_SPECS[key];

  // Find valid rarities for this type+level to guide the user
  const validRarities = [0,1,2,3].filter(r => ARTIFACT_SPECS[`${type}${level}${r}`]);

  if (!spec) {
    if (infoEl) infoEl.innerHTML = `<span style="color:var(--red)">⚠ T${level}${RARITY_LETTER[rarity]} doesn't exist for ${type}. Valid rarities: ${validRarities.map(r=>RARITY_NAME[r]).join(', ')}</span>`;
    if (stonesEl) stonesEl.innerHTML = '';
    return;
  }

  const numSlots = spec[1];
  if (infoEl) infoEl.innerHTML = `
    <div class="arti-editor-preview">
      ${artiImg(type, parseInt(level), 80)}
      <span style="color:var(--green)">✓ Effect: ${spec[0]} &nbsp;|&nbsp; ${numSlots} stone slot${numSlots!==1?'s':''}</span>
    </div>`;

  // Build stone selectors
  const prev = existingStones || state.artifact_set?.[slot]?.stones || [];
  stonesEl.innerHTML = Array.from({length: numSlots}, (_, i) => {
    const ps = prev[i] || {};
    return `<div class="stone-pair">
      <div class="stone-pair-img" id="ae-${slot}-stone-${i}-img">${ps.type ? stoneImg(ps.type + ps.level, 20) : ''}</div>
      <select id="ae-${slot}-stone-${i}-type" onchange="updateStoneImg('${slot}',${i})">
        <option value="">— stone —</option>
        ${STONE_TYPES.map(s => `<option value="${s}" ${s===ps.type?'selected':''}>${s}</option>`).join('')}
      </select>
      <select id="ae-${slot}-stone-${i}-lv" onchange="updateStoneImg('${slot}',${i})">
        ${[2,3,4].map(n => `<option value="${n}" ${n===ps.level?'selected':''}>${'lv'+n}</option>`).join('')}
      </select>
    </div>`;
  }).join('');
}

function updateStoneImg(slot, i) {
  const type = document.getElementById(`ae-${slot}-stone-${i}-type`)?.value || '';
  const lv   = document.getElementById(`ae-${slot}-stone-${i}-lv`)?.value   || '2';
  const imgEl = document.getElementById(`ae-${slot}-stone-${i}-img`);
  if (imgEl) imgEl.innerHTML = type ? stoneImg(type + lv, 20) : '';
}

async function applyArtifactEditor(slot) {
  const type   = document.getElementById(`ae-${slot}-type`)?.value || '';
  const level  = parseInt(document.getElementById(`ae-${slot}-level`)?.value) || 1;
  const rarity = parseInt(document.getElementById(`ae-${slot}-rarity`)?.value) || 0;

  if (!type) { await clearArtifactSlot(slot); return; }

  const key  = `${type}${level}${rarity}`;
  const spec = ARTIFACT_SPECS[key];
  if (!spec) { showToast(`Invalid artifact: ${type} T${level} ${RARITY_NAME[rarity]}`, true); return; }

  const numSlots = spec[1];
  const stones   = Array.from({length: 3}, (_, i) => {
    if (i >= numSlots) return { type:'', level:0 };
    const t = document.getElementById(`ae-${slot}-stone-${i}-type`)?.value || '';
    const l = parseInt(document.getElementById(`ae-${slot}-stone-${i}-lv`)?.value) || 2;
    return { type: t, level: t ? l : 0 };
  });

  await apiAction('/api/action/set_artifact', { slot, artifact: { type, level, rarity, stones } });
  document.getElementById(`art-editor-${slot}`)?.remove();
  showToast(`Artifact ${slot.toUpperCase()} set`);
}

async function clearArtifactSlot(slot) {
  await apiAction('/api/action/set_artifact', { slot, artifact: { type:'', level:0, rarity:0, stones:[{type:'',level:0},{type:'',level:0},{type:'',level:0}] } });
  document.getElementById(`art-editor-${slot}`)?.remove();
  showToast(`Slot ${slot.toUpperCase()} cleared`);
}

