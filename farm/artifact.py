class Stone:
    def __init__(self, type="", level=0):
        self.level = int(level)
        self.type = str(type)

        self.key = f"{self.type}{self.level}"

        if self.key not in stone_specs:
            raise KeyError(f"Unknown stone key: {self.key}")
        
    @property
    def effect(self):
        return float(stone_specs[self.key])

    @property
    def name(self):
        return f"T{self.level} {self.type.capitalize()}"
            


class Artifact:
    def __init__(self, type="", level=0, rarity=0, stone1=None, stone2=None, stone3=None):
        self.level = int(level)
        self.rarity = int(rarity)
        self.type = str(type)

        self.stone1 = stone1 if stone1 is not None else Stone()
        self.stone2 = stone2 if stone2 is not None else Stone()
        self.stone3 = stone3 if stone3 is not None else Stone()
        self.stones = [self.stone1, self.stone2, self.stone3]

        self.key = f"{self.type}{self.level}{self.rarity}"

        if self.key not in artifact_specs:
            raise KeyError(f"Unknown artifact key: {self.key}")

        # enforce number of slots
        allowed_slots = int(artifact_specs[self.key][1])
        if allowed_slots < 3 and self.stone3.type != "":
            raise ValueError(f"Artifact does not have 3 slots")
        if allowed_slots < 2 and self.stone2.type != "":
            raise ValueError(f"Artifact does not have 2 slots")
        if allowed_slots < 1 and self.stone1.type != "":
            raise ValueError(f"Artifact does not have 1 slot")

    @property
    def effect(self):
        return float(artifact_specs[self.key][0])

    @property
    def rarity_letter(self):
        if self.rarity == 0:
            return "C"
        if self.rarity == 1:
            return "R"
        if self.rarity == 2:
            return "E"
        if self.rarity == 3:
            return "L"
        return "X"

    @property
    def name(self):
        return f"T{self.level}{self.rarity_letter} {self.type.capitalize()}"
        

